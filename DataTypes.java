public class DataTypes{
	public static void main(String args[]){
		System.out.println("Data Tpyes");
		int n1=10;
		System.out.println("Data Type int="+n1);
		float n2=10.753475f;
		System.out.println("Data Type float="+n2);
		double n3=10.435345d;
		System.out.println("Data Type double="+n3);
		char n4='H';
		System.out.println("Data Type char="+n4);
		boolean n5=true;
		System.out.println("Data Type Boolean="+n5);
	}
}